create database bank;
use bank;

create table BRANCH
(
  BRANCH_ID int auto_increment
    primary key,
  ADDRESS   varchar(30) null,
  CITY      varchar(20) null,
  NAME      varchar(20) not null,
  STATE     varchar(10) null,
  ZIP_CODE  varchar(12) null
);

create table CUSTOMER
(
  CUST_ID      int auto_increment
    primary key,
  ADDRESS      varchar(30) null,
  CITY         varchar(20) null,
  CUST_TYPE_CD varchar(1)  not null,
  FED_ID       varchar(12) not null,
  POSTAL_CODE  varchar(10) null,
  STATE        varchar(20) null
);

create table BUSINESS
(
  INCORP_DATE date         null,
  NAME        varchar(255) not null,
  STATE_ID    varchar(10)  not null,
  CUST_ID     int          not null
    primary key,
  constraint BUSINESS_EMPLOYEE_FK
  foreign key (CUST_ID) references CUSTOMER (CUST_ID)
);

create table DEPARTMENT
(
  DEPT_ID int auto_increment
    primary key,
  NAME    varchar(20) not null
);

create table EMPLOYEE
(
  EMP_ID             int auto_increment
    primary key,
  END_DATE           date        null,
  FIRST_NAME         varchar(20) not null,
  LAST_NAME          varchar(20) not null,
  START_DATE         date        not null,
  TITLE              varchar(20) null,
  ASSIGNED_BRANCH_ID int         null,
  DEPT_ID            int         null,
  SUPERIOR_EMP_ID    int         null,
  constraint EMPLOYEE_BRANCH_FK
  foreign key (ASSIGNED_BRANCH_ID) references BRANCH (BRANCH_ID),
  constraint EMPLOYEE_DEPARTMENT_FK
  foreign key (DEPT_ID) references DEPARTMENT (DEPT_ID),
  constraint EMPLOYEE_EMPLOYEE_FK
  foreign key (SUPERIOR_EMP_ID) references EMPLOYEE (EMP_ID)
);

create index EMPLOYEE_BRANCH_FK
  on EMPLOYEE (ASSIGNED_BRANCH_ID);

create index EMPLOYEE_DEPARTMENT_FK
  on EMPLOYEE (DEPT_ID);

create index EMPLOYEE_EMPLOYEE_FK
  on EMPLOYEE (SUPERIOR_EMP_ID);

create table INDIVIDUAL
(
  BIRTH_DATE date        null,
  FIRST_NAME varchar(30) not null,
  LAST_NAME  varchar(30) not null,
  CUST_ID    int         not null
    primary key,
  constraint INDIVIDUAL_CUSTOMER_FK
  foreign key (CUST_ID) references CUSTOMER (CUST_ID)
);

create table OFFICER
(
  OFFICER_ID int auto_increment
    primary key,
  END_DATE   date        null,
  FIRST_NAME varchar(30) not null,
  LAST_NAME  varchar(30) not null,
  START_DATE date        not null,
  TITLE      varchar(20) null,
  CUST_ID    int         null,
  constraint OFFICER_CUSTOMER_FK
  foreign key (CUST_ID) references CUSTOMER (CUST_ID)
);

create index OFFICER_CUSTOMER_FK
  on OFFICER (CUST_ID);

create table PRODUCT_TYPE
(
  PRODUCT_TYPE_CD varchar(255) not null
    primary key,
  NAME            varchar(50)  null
);

create table PRODUCT
(
  PRODUCT_CD      varchar(10)  not null
    primary key,
  DATE_OFFERED    date         null,
  DATE_RETIRED    date         null,
  NAME            varchar(50)  not null,
  PRODUCT_TYPE_CD varchar(255) null,
  constraint PRODUCT_PRODUCT_TYPE_FK
  foreign key (PRODUCT_TYPE_CD) references PRODUCT_TYPE (PRODUCT_TYPE_CD)
);

create table ACCOUNT
(
  ACCOUNT_ID         int auto_increment
    primary key,
  AVAIL_BALANCE      float       null,
  CLOSE_DATE         date        null,
  LAST_ACTIVITY_DATE date        null,
  OPEN_DATE          date        not null,
  PENDING_BALANCE    float       null,
  STATUS             varchar(10) null,
  CUST_ID            int         null,
  OPEN_BRANCH_ID     int         not null,
  OPEN_EMP_ID        int         not null,
  PRODUCT_CD         varchar(10) not null,
  constraint ACCOUNT_CUSTOMER_FK
  foreign key (CUST_ID) references CUSTOMER (CUST_ID),
  constraint ACCOUNT_BRANCH_FK
  foreign key (OPEN_BRANCH_ID) references BRANCH (BRANCH_ID),
  constraint ACCOUNT_EMPLOYEE_FK
  foreign key (OPEN_EMP_ID) references EMPLOYEE (EMP_ID),
  constraint ACCOUNT_PRODUCT_FK
  foreign key (PRODUCT_CD) references PRODUCT (PRODUCT_CD)
);

create index ACCOUNT_BRANCH_FK
  on ACCOUNT (OPEN_BRANCH_ID);

create index ACCOUNT_CUSTOMER_FK
  on ACCOUNT (CUST_ID);

create index ACCOUNT_EMPLOYEE_FK
  on ACCOUNT (OPEN_EMP_ID);

create index ACCOUNT_PRODUCT_FK
  on ACCOUNT (PRODUCT_CD);

create table ACC_TRANSACTION
(
  TXN_ID              bigint auto_increment
    primary key,
  AMOUNT              float       not null,
  FUNDS_AVAIL_DATE    datetime    not null,
  TXN_DATE            datetime    not null,
  TXN_TYPE_CD         varchar(10) null,
  ACCOUNT_ID          int         null,
  EXECUTION_BRANCH_ID int         null,
  TELLER_EMP_ID       int         null,
  constraint ACC_TRANSACTION_ACCOUNT_FK
  foreign key (ACCOUNT_ID) references ACCOUNT (ACCOUNT_ID),
  constraint ACC_TRANSACTION_BRANCH_FK
  foreign key (EXECUTION_BRANCH_ID) references BRANCH (BRANCH_ID),
  constraint ACC_TRANSACTION_EMPLOYEE_FK
  foreign key (TELLER_EMP_ID) references EMPLOYEE (EMP_ID)
);

create index ACC_TRANSACTION_ACCOUNT_FK
  on ACC_TRANSACTION (ACCOUNT_ID);

create index ACC_TRANSACTION_BRANCH_FK
  on ACC_TRANSACTION (EXECUTION_BRANCH_ID);

create index ACC_TRANSACTION_EMPLOYEE_FK
  on ACC_TRANSACTION (TELLER_EMP_ID);

create index PRODUCT_PRODUCT_TYPE_FK
  on PRODUCT (PRODUCT_TYPE_CD);

